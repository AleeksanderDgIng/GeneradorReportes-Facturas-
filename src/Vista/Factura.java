    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        
        if(!txtCantidad.getText().equals("0")){
            if( ctrl.RegistrarProducto(jcbIDproducto.getSelectedItem().toString(),
                txtNumFactura.getText(),
                txtCantidad.getText(),
                pPrecio.getText(),
                txtTotal.getText())
        ){   
            
            JOptionPane.showMessageDialog(this, "Exito: Se registo el producto en la nueva Venta");
            updateTabla(); 
            txtCantidad.setText("0");
            txtTotal.setText("0");
            
            
        }else{
            JOptionPane.showMessageDialog(this, "Error: No se pudo registrar venta de producto...");
        }
        }else{
            JOptionPane.showMessageDialog(this, "Error: La cantidad debe ser mayor a cero");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    
    
    private void updateTabla(){
        //actualiza los datos de la tabla realizando una consulta a la base de datos
        String[] columNames = {"id producto","cantidad","precio por Unidad","Total"};        
        //nombre de columnas, tabla, instruccion sql 
        
        dtDetalle = ctrl.getDetalle(txtNumFactura.getText());
        // se colocan los datos en la tabla
        
        DefaultTableModel datos = new DefaultTableModel(dtDetalle,columNames);
        tabla.setModel(datos);
    }
    
       
    
    private void jcbIDproductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcbIDproductoActionPerformed
        if(jcbIDproducto.getSelectedItem()!=null){
            //obtiene los datos del producto seleccionado y los coloca en los controles
            Object[][] datos = ctrl.getDataProducto(jcbIDproducto.getSelectedItem().toString());
            pName.setText(datos[0][0].toString());
            pPrecio.setText(datos[0][1].toString());
            pCantidad.setText(datos[0][2].toString());
            txtCantidad.setText("0");
            txtTotal.setText("0");
        }
        
    }//GEN-LAST:event_jcbIDproductoActionPerformed

