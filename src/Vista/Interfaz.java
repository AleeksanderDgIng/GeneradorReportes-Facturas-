    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String Res = JOptionPane.showInputDialog(this,"Ingrese ID cliente: ");
    if(ctrl.existe_cliente(Res)){
        if(estacerrado(f)){
            JOptionPane.showMessageDialog(null,"Bienvenido, factura para el cliente: " + ctrl.getNameCliente());
            f = new Factura(ctrl);
            panel.add(f);
            f.show();
        }
        else{
            JOptionPane.showMessageDialog(this,"Error: La ventana ya esta abierta...");
        }
    }else{
            JOptionPane.showMessageDialog(this,"Error: El ID del cliente es incorrecto...");
    }
    }//GEN-LAST:event_jButton1ActionPerformed

